void DumpData(size_t size, uint8_t *buffer)
{
  char str[64];

  while(size)
  {
    char *s = str;
    size_t i = size;
    if (i > 16) i = 16;
    size -= i;
    while(i--)
      s += sprintf(s, " %02X", *buffer++);
    puts(str);
  }
}

