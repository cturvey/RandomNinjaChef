void ProcessRTCM3(uint32_t Size, uint8_t *Buffer) // Larger Buffer, Stream would be more complicated
{
  while(Size)
  {
    uint32_t Consumed;
    while(Size && (*Buffer != 0xD3)) // Skip to Sync
    {
      Size--;
      Buffer++;
    }
    if (!Size) break; // All gone
    Consumed = DumpRTCM3(Size, Buffer);
    if (!Consumed) break; // Leave when exhausted, stream would handle a pull-down buffer
    Size -= min(Size, Consumed); // Robustly close-out
    Buffer += Consumed;
  }
}

